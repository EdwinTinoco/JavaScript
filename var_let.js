// let -> puedes reasignar otro valor pero no puedes redeclarar la variable otra vez
// const -> no puedes reasignar, ni redeclarar

// Var variables can be re-defined o re-declare
var age = 12;
console.log(age);

var age = 15; // yep!
console.log(age);

// Let variables cannot be re-defined o re-declare
let age = 12;
console.log(age);

let age = 15; // nope!
console.log(age);
